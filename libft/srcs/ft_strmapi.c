#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*tab;
	int		size;
	int		i;

	size = ft_strlen((char *)s) + 1;
	tab = malloc(size * sizeof(char));
	if (!tab)
		return (0);
	i = 0;
	while (s[i])
	{
		tab[i] = f(i, s[i]);
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
